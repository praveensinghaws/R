# R Boolean or Logical Values:

# Example of Logical Conditions
result1 <- 10 > 9  # True, because 10 is greater than 9
result2 <- 10 < 9  # False, because 10 is not less than 9
result3 <- 10 == 9 # False, because 10 is not equal to 9
result4 <- 10 != 9 # True, because 10 is not equal to 9

# Display the results
result1
result2
result3
result4



