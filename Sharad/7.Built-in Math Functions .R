# Simple math in R

# Basic arithmetic operations
10 + 5    # Addition
10 - 5    # Subtraction
10 * 5    # Multiplication
10 / 5    # Division

# Built-in math functions: min() and max()
min_value <- min(10, 30, 5, 90, 100)  # Find the minimum value
max_value <- max(10, 30, 5, 90, 100)  # Find the maximum value

# Display the results
min_value
max_value

# Additional math functions
sqrt_result <- sqrt(64)      # Square root
abs_result <- abs(-20.6)     # Absolute value
ceiling_result <- ceiling(10.4)   # Round up to the nearest integer
floor_result <- floor(10.4)   # Round down to the nearest integer

# Display the results
sqrt_result
abs_result
ceiling_result
floor_result
